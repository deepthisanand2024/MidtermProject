"""Module providing a function printing sum and difference."""

def add(a,b):
    """Function printing sum of 2 numbers."""
    return a + b
def multiply(a,b):
    """Function printing product of 2 numbers."""
    return a * b
def subtract(a,b):
    """Function printing difference of 2 numbers."""
    return a - b
def divide(a,b):
    """Function printing difference of 2 numbers."""
    return a / b
